
#include <stdio.h>

int main(){
    int n;
    scanf("%d",&n);
    int a;
    a=(2*n)-1;
    printf("a=%d\n",a);
    printf("______________________________\n");
    int i,j,k;
    for(i=1;i<=a;i++)
    {
        for(j=a;j>=1;j--)
        {
        for(k=n;k>=1;k--)
        printf("%d ",n);
            
        }
        
        printf("\n");
    }
    

    
}